// This part of the code defines a function
function multiply(a,b) { 
    return a * b
} 
function add(a,b) { 
    return a + b
}
function subtract(a,b) { 
    return a - b
} 
function divide(a,b) { 
    return a / b
}
function square(a) { 
    return a ** 2
}
function cube(a) { 
    return a ** 3
}
function square_n_times(number,n) { 
    return number ** n
}
